x = 5 
y = 7

print("Before Swapping: ")
print("Value of x = ", x)
print("Value of y = ", y)

x = x + y
y = x - y
x = x - y

print("After Swapping: ")
print("Value of x = ", x)
print("Value of y = ", y)
